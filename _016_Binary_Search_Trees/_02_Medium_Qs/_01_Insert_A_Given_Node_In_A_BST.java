package _016_Binary_Search_Trees._02_Medium_Qs;

public class _01_Insert_A_Given_Node_In_A_BST {

    /*
    =================================================================================================
                                INSERT A GIVEN NODE IN BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------
    1. Root node of a Binary Search Tree (BST)
    2. A value val to insert

    Task:
    -------------------------
    Insert the given value into the BST while maintaining BST properties.

    Return:
    -------------------------
    Updated root node of the BST.

    -------------------------------------------------------------------------------------------------

    IMPORTANT:
    -------------------------
    It is guaranteed that:
    1. All values are unique.
    2. val does not already exist in BST.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                    4
                  /   \
                 2     7
                / \
               1   3

    val = 5

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

                    4
                  /   \
                 2     7
                / \   /
               1   3 5

    Explanation:
    -------------------------
    5 > 4  -> Move Right
    5 < 7  -> Move Left

    Left child of 7 is NULL.

    Insert 5 there.

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            40
                          /    \
                        20      60
                       / \     / \
                     10  30   50  70

    val = 25

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

                            40
                          /    \
                        20      60
                       / \     / \
                     10  30   50  70
                         /
                        25

    Explanation:
    -------------------------

    25 < 40 -> Move Left
    25 > 20 -> Move Right
    25 < 30 -> Move Left

    Left child of 30 is NULL.

    Insert 25 there.

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                        4
                      /   \
                     2     7
                    /     /
                   1     6

    val = 3

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

                        4
                      /   \
                     2     7
                    / \   /
                   1   3 6

    Correct Answer:
    -------------------------

            [4, 2, 7, 1, 3, 6]

    =================================================================================================
                                            INTUITION
    =================================================================================================

    BST Property:
    -------------------------

            Left Subtree < Node < Right Subtree

    While inserting:
    -------------------------
    We must place the new value at a position
    where BST property remains valid.

    -------------------------------------------------------------------------------------------------

    INSERTION LOGIC
    -------------------------------------------------------------------------------------------------

    CASE 1:
    -------------------------
    val < current node value

    Then:
    -------------------------
    Move LEFT.

    -------------------------------------------------------------------------------------------------

    CASE 2:
    -------------------------
    val > current node value

    Then:
    -------------------------
    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    CASE 3:
    -------------------------
    Reached NULL position

    Then:
    -------------------------
    Create new node and insert it there.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Start traversal from root.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Compare val with current node.

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    If:
    -------------------------
    val < current.data

    Then:
    -------------------------
    Move to left subtree.

    -------------------------------------------------------------------------------------------------

    Else If:
    -------------------------
    val > current.data

    Then:
    -------------------------
    Move to right subtree.

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------
    Continue traversal recursively
    until NULL node is found.

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Create new node and attach it.

    -------------------------------------------------------------------------------------------------

    STEP 6:
    -------------------------
    Return updated BST root.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    BST:
    -------------------------

                        4
                      /   \
                     2     7
                    / \
                   1   3

    val = 5

    -------------------------------------------------------------------------------------------------

    CALL 1:
    -------------------------

    Current Node = 4

    Compare:
    -------------------------
    5 > 4

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    CALL 2:
    -------------------------

    Current Node = 7

    Compare:
    -------------------------
    5 < 7

    Move LEFT.

    -------------------------------------------------------------------------------------------------

    CALL 3:
    -------------------------

    Current Node = NULL

    NULL position found.

    Create:
    -------------------------

            new TreeNode(5)

    Insert node.

    -------------------------------------------------------------------------------------------------

    FINAL TREE:
    -------------------------

                        4
                      /   \
                     2     7
                    / \   /
                   1   3 5

    =================================================================================================
                                            VISUAL FLOW
    =================================================================================================

    Search Path:
    -------------------------

            4 -> 7 -> NULL

    Insert:
    -------------------------

            5 as left child of 7

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for BST Node
    static class TreeNode {

        int data;
        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;
            left = null;
            right = null;
        }
    }

    static class Solution {

        /*
        =============================================================================================
                                    RECURSIVE INSERT FUNCTION
        =============================================================================================
        */

        private TreeNode solve(TreeNode node, int val) {

            /*
            Base Case:
            -------------------------
            If NULL position found,
            create new node.
            */

            if (node == null) {
                return new TreeNode(val);
            }

            /*
            If value is smaller,
            move to left subtree.
            */

            if (val < node.data) {

                node.left = solve(node.left, val);
            }

            /*
            If value is greater,
            move to right subtree.
            */

            else if (val > node.data) {

                node.right = solve(node.right, val);
            }

            /*
            Return current node after insertion.
            */

            return node;
        }

        /*
        =============================================================================================
                                        MAIN INSERT FUNCTION
        =============================================================================================
        */

        public TreeNode insertIntoBST(TreeNode root, int val) {

            return solve(root, val);
        }
    }

    /*
    =================================================================================================
                                            INORDER TRAVERSAL
    =================================================================================================

    Inorder traversal of BST gives sorted order.

    LEFT -> ROOT -> RIGHT
    */

    public static void printInOrder(TreeNode root) {

        if (root == null) {
            return;
        }

        printInOrder(root.left);

        System.out.print(root.data + " ");

        printInOrder(root.right);
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                            4
                          /   \
                         2     7
                        / \
                       1   3
        */

        TreeNode root = new TreeNode(4);

        root.left = new TreeNode(2);
        root.right = new TreeNode(7);

        root.left.left = new TreeNode(1);
        root.left.right = new TreeNode(3);

        int val = 5;

        Solution solution = new Solution();

        /*
        Insert value into BST
        */

        TreeNode newRoot = solution.insertIntoBST(root, val);

        /*
        Print inorder traversal.
        */

        System.out.println("Inorder Traversal After Insertion:");

        printInOrder(newRoot);
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------
    O(H)

    where:
    -------------------------
    H = Height of BST

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------
    Height = log N

    Therefore:
    -------------------------
    O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------
    Skewed BST

    Example:
    -------------------------

                10
                  \
                   20
                     \
                      30
                        \
                         40

    Height becomes:
    -------------------------
    O(N)

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------
    O(H)

    Reason:
    -------------------------
    Recursive stack space.

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------
    O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------
    O(N)

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. BST insertion maintains ordering property.

    2. Smaller values go LEFT.

    3. Larger values go RIGHT.

    4. Insertion always occurs at NULL position.

    5. Inorder traversal after insertion remains sorted.

    6. Recursive solution is simple and elegant.

    =================================================================================================
    */

}