package _016_Binary_Search_Trees._02_Medium_Qs;

public class _05_LCA_in_BST {

    /*
    =================================================================================================
                                        LCA IN BST
    =================================================================================================

    FULL FORM:
    -------------------------------------------------------------------------------------------------

            LCA = Lowest Common Ancestor

    =================================================================================================
                                        PROBLEM STATEMENT
    =================================================================================================

    Given:
    -------------------------
    1. Root node of a Binary Search Tree (BST)
    2. Two node values p and q

    Task:
    -------------------------
    Find the Lowest Common Ancestor (LCA)
    of the two given nodes.

    -------------------------------------------------------------------------------------------------

    Lowest Common Ancestor:
    -------------------------
    The lowest node in the tree that has
    both p and q as descendants.

    NOTE:
    -------------------------
    A node can also be a descendant of itself.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       2   4     7

    p = 2
    q = 4

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            3

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    Path to 2:
    -------------------------

            5 -> 3 -> 2

    Path to 4:
    -------------------------

            5 -> 3 -> 4

    Lowest common node:
    -------------------------

            3

    Therefore:
    -------------------------

            LCA = 3

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       2   4     7

    p = 2
    q = 7

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            5

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    2 lies in LEFT subtree of 5.

    7 lies in RIGHT subtree of 5.

    Therefore:
    -------------------------

            5 is the first common ancestor.

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                            2
                           / \
                          1   4
                             / \
                            3   6

    p = 1
    q = 6

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            2

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            2

    =================================================================================================
                                            INTUITION
    =================================================================================================

    BST Property:
    -------------------------

            LEFT < ROOT < RIGHT

    -------------------------------------------------------------------------------------------------

    This property helps us locate the LCA efficiently.

    =================================================================================================
                                    THREE IMPORTANT CASES
    =================================================================================================

    CASE 1:
    -------------------------------------------------------------------------------------------------

    Both nodes are SMALLER than current node.

    Example:
    -------------------------

                            10
                           /
                          5
                         / \
                        2   7

    p = 2
    q = 7

    -------------------------------------------------------------------------------------------------

    Since:
    -------------------------

            p < 10
            q < 10

    Both nodes lie in LEFT subtree.

    Therefore:
    -------------------------

            Move LEFT.

    =================================================================================================
                                    CASE 2
    =================================================================================================

    Both nodes are GREATER than current node.

    Example:
    -------------------------

                            10
                              \
                               15
                              /  \
                             12  20

    p = 12
    q = 20

    -------------------------------------------------------------------------------------------------

    Since:
    -------------------------

            p > 10
            q > 10

    Both nodes lie in RIGHT subtree.

    Therefore:
    -------------------------

            Move RIGHT.

    =================================================================================================
                                    CASE 3
    =================================================================================================

    Nodes are on DIFFERENT SIDES.

    Example:
    -------------------------

                            10
                           /  \
                          5    15

    p = 5
    q = 15

    -------------------------------------------------------------------------------------------------

    Since:
    -------------------------

            p < 10
            q > 10

    One node is LEFT,
    one node is RIGHT.

    Therefore:
    -------------------------

            Current node is LCA.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Start from root node.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Compare p and q with current node.

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    If:
    -------------------------

            p < current
            q < current

    Then:
    -------------------------

            Move LEFT

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------

    If:
    -------------------------

            p > current
            q > current

    Then:
    -------------------------

            Move RIGHT

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Otherwise:

    Current node becomes LCA.

    -------------------------------------------------------------------------------------------------

    STEP 6:
    -------------------------
    Return current node.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    TREE:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       2   4     7

    p = 2
    q = 4

    =================================================================================================
                                    STEP-BY-STEP EXECUTION
    =================================================================================================

    Current Node:
    -------------------------

            5

    -------------------------------------------------------------------------------------------------

    Compare:
    -------------------------

            2 < 5
            4 < 5

    Both smaller.

    Move LEFT.

    =================================================================================================

    Current Node:
    -------------------------

            3

    -------------------------------------------------------------------------------------------------

    Compare:
    -------------------------

            2 < 3
            4 > 3

    Nodes are on different sides.

    Therefore:
    -------------------------

            3 is the LCA.

    =================================================================================================
                                            VISUAL FLOW
    =================================================================================================

    Search Path:
    -------------------------

            5 -> 3

    At node 3:
    -------------------------

            One node LEFT
            One node RIGHT

    Therefore:
    -------------------------

            STOP

            LCA = 3

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for BST Node
    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            this.data = val;

            this.left = null;
            this.right = null;
        }
    }

    static class Solution {

        /*
        =============================================================================================
                                    LCA FUNCTION
        =============================================================================================
        */

        TreeNode lca(TreeNode root, int p, int q) {

            /*
            Base Case:
            Empty tree.
            */

            if (root == null) {

                return null;
            }

            /*
            Store current node value.
            */

            int curr = root.data;

            /*
            CASE 1:
            Both nodes greater than current node.

            Move RIGHT.
            */

            if (curr < p && curr < q) {

                return lca(root.right, p, q);
            }

            /*
            CASE 2:
            Both nodes smaller than current node.

            Move LEFT.
            */

            if (curr > p && curr > q) {

                return lca(root.left, p, q);
            }

            /*
            CASE 3:
            Nodes are on different sides
            OR one node equals current node.

            Current node is LCA.
            */

            return root;
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                4
                              /   \
                             2     7
                            / \   / \
                           1   3 5   8
        */

        TreeNode root = new TreeNode(4);

        root.left = new TreeNode(2);
        root.right = new TreeNode(7);

        root.left.left = new TreeNode(1);
        root.left.right = new TreeNode(3);

        root.right.left = new TreeNode(5);
        root.right.right = new TreeNode(8);

        Solution sol = new Solution();

        /*
        Find LCA of 5 and 8
        */

        TreeNode ans = sol.lca(root, 5, 8);

        /*
        Print result
        */

        if (ans != null) {

            System.out.println(
                    "LCA(5, 8) = " + ans.data
            );
        }

        else {

            System.out.println(
                    "LCA not found"
            );
        }
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------

            O(H)

    where:
    -------------------------

            H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    At every step,
    we move only in ONE direction.

    Either:
    -------------------------

            LEFT

    OR:
    -------------------------

            RIGHT

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------

            H = log N

    Therefore:
    -------------------------

            O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------
    Skewed BST

    Example:
    -------------------------

                10
                  \
                   20
                     \
                      30
                        \
                         40

    Height becomes:
    -------------------------

            O(N)

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(H)

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    Recursive stack space.

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------

            O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------

            O(N)

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. BST property helps optimize LCA search.

    2. If both nodes are smaller:
       Move LEFT.

    3. If both nodes are greater:
       Move RIGHT.

    4. If nodes split on different sides:
       Current node is LCA.

    5. LCA can also be one of the given nodes.

    6. BST-based LCA is more efficient than
       normal Binary Tree LCA.

    =================================================================================================
    */

}