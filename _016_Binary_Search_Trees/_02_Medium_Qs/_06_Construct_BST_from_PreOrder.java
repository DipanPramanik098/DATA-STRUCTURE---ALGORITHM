package _016_Binary_Search_Trees._02_Medium_Qs;

public class _06_Construct_BST_from_PreOrder {

    /*
    =================================================================================================
                            CONSTRUCT BST FROM PREORDER TRAVERSAL
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------
    A preorder traversal array of a Binary Search Tree (BST).

    Task:
    -------------------------
    Construct the BST and return its root node.

    -------------------------------------------------------------------------------------------------

    NOTE:
    -------------------------
    It is guaranteed that a valid BST can always
    be formed from the given preorder traversal.

    =================================================================================================
                                        WHAT IS PREORDER?
    =================================================================================================

    Preorder Traversal follows:

            ROOT -> LEFT -> RIGHT

    -------------------------------------------------------------------------------------------------

    Example:
    -------------------------

                            8
                          /   \
                         5     10
                        / \      \
                       1   7      12

    -------------------------------------------------------------------------------------------------

    Preorder Traversal:
    -------------------------

            [8, 5, 1, 7, 10, 12]

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

            preorder = [8, 5, 1, 7, 10, 12]

    -------------------------------------------------------------------------------------------------

    Output BST:
    -------------------------

                            8
                          /   \
                         5     10
                        / \      \
                       1   7      12

    -------------------------------------------------------------------------------------------------

    Output Array Representation:
    -------------------------

            [8, 5, 10, 1, 7, null, 12]

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

            preorder = [1, 3]

    -------------------------------------------------------------------------------------------------

    Output BST:
    -------------------------

                        1
                         \
                          3

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [1, null, 3]

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

            preorder = [5, 3, 2, 4, 6, 7]

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [5, 3, 6, 2, 4, null, 7]

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            [5, 3, 6, 2, 4, null, 7]

    =================================================================================================
                                        PRE-REQUISITE
    =================================================================================================

    Understanding BST Property:
    -------------------------

            LEFT < ROOT < RIGHT

    -------------------------------------------------------------------------------------------------

    This property is extremely important for solving this problem.

    =================================================================================================
                                            INTUITION
    =================================================================================================

    Key Observation:
    -------------------------

    In preorder traversal:

            First element is always ROOT.

    -------------------------------------------------------------------------------------------------

    Example:
    -------------------------

            preorder = [8, 5, 1, 7, 10, 12]

    -------------------------------------------------------------------------------------------------

    Root:
    -------------------------

            8

    -------------------------------------------------------------------------------------------------

    Values smaller than 8:
    -------------------------

            5, 1, 7

    These belong to LEFT subtree.

    -------------------------------------------------------------------------------------------------

    Values greater than 8:
    -------------------------

            10, 12

    These belong to RIGHT subtree.

    =================================================================================================
                                        CORE IDEA
    =================================================================================================

    While constructing BST:

    -------------------------------------------------------------------------------------------------

    LEFT subtree values:
    -------------------------

            Must be smaller than root.

    -------------------------------------------------------------------------------------------------

    RIGHT subtree values:
    -------------------------

            Must be greater than root.

    -------------------------------------------------------------------------------------------------

    We use an UPPER BOUND to maintain BST validity.

    =================================================================================================
                                        UPPER BOUND CONCEPT
    =================================================================================================

    Suppose current root is:

            8

    -------------------------------------------------------------------------------------------------

    LEFT subtree:
    -------------------------

            Values must be < 8

    Therefore:
    -------------------------

            Bound = 8

    -------------------------------------------------------------------------------------------------

    RIGHT subtree:
    -------------------------

            Values can go until parent's bound.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Maintain an index pointer for preorder array.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Create recursive helper function.

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------
    If:
            index reaches array size

    OR

            preorder[index] > bound

    Then:
            return null

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------
    Create root node using current preorder value.

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Move index forward.

    -------------------------------------------------------------------------------------------------

    STEP 6:
    -------------------------
    Construct LEFT subtree.

    Bound becomes:
            current node value

    -------------------------------------------------------------------------------------------------

    STEP 7:
    -------------------------
    Construct RIGHT subtree.

    Bound remains same.

    -------------------------------------------------------------------------------------------------

    STEP 8:
    -------------------------
    Return root node.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    PREORDER:
    -------------------------

            [8, 5, 1, 7, 10, 12]

    =================================================================================================
                                    STEP-BY-STEP EXECUTION
    =================================================================================================

    INDEX = 0

    Current Value:
    -------------------------

            8

    Create Root:
    -------------------------

            8

    =================================================================================================

    LEFT SUBTREE OF 8
    -------------------------------------------------------------------------------------------------

    Bound = 8

    Current Value:
    -------------------------

            5

    Since:
            5 < 8

    Create Node:
    -------------------------

            5

    =================================================================================================

    LEFT SUBTREE OF 5
    -------------------------------------------------------------------------------------------------

    Bound = 5

    Current Value:
    -------------------------

            1

    Since:
            1 < 5

    Create Node:
    -------------------------

            1

    =================================================================================================

    Next Value:
    -------------------------

            7

    But:
            7 > 5

    Therefore:
            Stop LEFT subtree.

    =================================================================================================

    RIGHT SUBTREE OF 5
    -------------------------------------------------------------------------------------------------

    Bound = 8

    Current Value:
    -------------------------

            7

    Since:
            7 < 8

    Create Node:
    -------------------------

            7

    =================================================================================================

    Continue similarly for:
    -------------------------

            10
            12

    =================================================================================================

    Final BST:
    -------------------------

                            8
                          /   \
                         5     10
                        / \      \
                       1   7      12

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for Tree Node
    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;

            left = null;
            right = null;
        }
    }

    static class Solution {

        /*
        =============================================================================================
                                MAIN FUNCTION
        =============================================================================================
        */

        public TreeNode bstFromPreorder(int[] preorder) {

            /*
            Index array is used because Java passes
            arrays by reference.

            index[0] keeps track of current element.
            */

            int[] index = {0};

            /*
            Start recursive construction.

            Initial bound = Integer.MAX_VALUE
            */

            return buildBST(
                    preorder,
                    Integer.MAX_VALUE,
                    index
            );
        }

        /*
        =============================================================================================
                                RECURSIVE HELPER FUNCTION
        =============================================================================================
        */

        private TreeNode buildBST(
                int[] preorder,
                int bound,
                int[] index
        ) {

            /*
            Base Case 1:
            All elements processed.
            */

            if (index[0] == preorder.length) {

                return null;
            }

            /*
            Base Case 2:
            Current value exceeds bound.

            Means:
            This node cannot belong to this subtree.
            */

            if (preorder[index[0]] > bound) {

                return null;
            }

            /*
            Create node using current preorder value.
            */

            TreeNode root =
                    new TreeNode(preorder[index[0]]);

            /*
            Move to next preorder element.
            */

            index[0]++;

            /*
            Construct LEFT subtree.

            New bound = current node value.
            */

            root.left =
                    buildBST(
                            preorder,
                            root.data,
                            index
                    );

            /*
            Construct RIGHT subtree.

            Bound remains same.
            */

            root.right =
                    buildBST(
                            preorder,
                            bound,
                            index
                    );

            /*
            Return constructed subtree.
            */

            return root;
        }
    }

    /*
    =================================================================================================
                                        INORDER PRINT
    =================================================================================================

    Inorder traversal of BST gives sorted order.
    */

    public static void inorder(TreeNode root) {

        if (root == null) {

            return;
        }

        inorder(root.left);

        System.out.print(root.data + " ");

        inorder(root.right);
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
        Preorder Traversal:
                [8, 5, 1, 7, 10, 12]
        */

        int[] preorder = {
                8, 5, 1, 7, 10, 12
        };

        Solution solution = new Solution();

        /*
        Construct BST
        */

        TreeNode root =
                solution.bstFromPreorder(preorder);

        /*
        Print inorder traversal.

        Expected Sorted Output:
                1 5 7 8 10 12
        */

        System.out.println(
                "Inorder Traversal of Constructed BST:"
        );

        inorder(root);
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------

            O(N)

    where:
    -------------------------

            N = Number of nodes

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Every node is visited exactly once.

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(H)

    where:
    -------------------------

            H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Recursive stack space.

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------

            H = log N

    Therefore:
    -------------------------

            O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------

            O(N)

    when BST becomes skewed.

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Preorder traversal starts with ROOT.

    2. BST property:
            LEFT < ROOT < RIGHT

    3. Bound helps maintain BST validity.

    4. Left subtree uses tighter bound.

    5. Right subtree keeps parent's bound.

    6. Each node processed only once.

    7. Extremely efficient O(N) solution.

    =================================================================================================
    */

}