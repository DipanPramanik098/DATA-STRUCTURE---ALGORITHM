package _016_Binary_Search_Trees._02_Medium_Qs;

public class _07_Inorder_Successor_And_Predecessor_in_BST {

    /*
    =================================================================================================
                        INORDER SUCCESSOR AND PREDECESSOR IN BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------
    1. Root node of a Binary Search Tree (BST)
    2. An integer key

    Task:
    -------------------------
    Find:
            1. Inorder Predecessor
            2. Inorder Successor

    of the given key in BST.

    -------------------------------------------------------------------------------------------------

    Return:
    -------------------------

            [predecessor, successor]

    -------------------------------------------------------------------------------------------------

    If predecessor does not exist:
    -------------------------

            return -1

    -------------------------------------------------------------------------------------------------

    If successor does not exist:
    -------------------------

            return -1

    =================================================================================================
                                        DEFINITIONS
    =================================================================================================

    INORDER PREDECESSOR:
    -------------------------------------------------------------------------------------------------

    Largest value SMALLER than given key.

    -------------------------------------------------------------------------------------------------

    Example:
    -------------------------

            BST Inorder Traversal:

                    1 2 4 5 7 10 12

    Key:
    -------------------------

            10

    Predecessor:
    -------------------------

            7

    because:
            7 is largest value smaller than 10.

    =================================================================================================
                                    INORDER SUCCESSOR
    =================================================================================================

    Smallest value GREATER than given key.

    -------------------------------------------------------------------------------------------------

    Example:
    -------------------------

            BST Inorder Traversal:

                    1 2 4 5 7 10 12

    Key:
    -------------------------

            10

    Successor:
    -------------------------

            12

    because:
            12 is smallest value greater than 10.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         2     10
                        / \    / \
                       1   4  7  12

    key = 10

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [7, 12]

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    Inorder Traversal:
    -------------------------

            1 2 4 5 7 10 12

    -------------------------------------------------------------------------------------------------

    Largest value smaller than 10:
    -------------------------

            7

    -------------------------------------------------------------------------------------------------

    Smallest value greater than 10:
    -------------------------

            12

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         2     10
                        / \    / \
                       1   4  7  12

    key = 12

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [10, -1]

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    12 is largest node.

    Therefore:
    -------------------------

            No successor exists.

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         2     10
                        / \    / \
                       1   4  7  12

    key = 1

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [-1, 2]

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            [-1, 2]

    =================================================================================================
                                            INTUITION
    =================================================================================================

    BST Property:
    -------------------------

            LEFT < ROOT < RIGHT

    -------------------------------------------------------------------------------------------------

    This property helps us efficiently find
    predecessor and successor.

    =================================================================================================
                                    IMPORTANT OBSERVATION
    =================================================================================================

    PREDECESSOR:
    -------------------------------------------------------------------------------------------------

    Any node smaller than key can become
    potential predecessor.

    We want:
    -------------------------

            Largest among smaller values

    -------------------------------------------------------------------------------------------------

    Therefore:
    -------------------------

            Move RIGHT whenever key is greater.

    =================================================================================================
                                    SUCCESSOR
    =================================================================================================

    Any node larger than key can become
    potential successor.

    We want:
    -------------------------

            Smallest among larger values

    -------------------------------------------------------------------------------------------------

    Therefore:
    -------------------------

            Move LEFT whenever key is smaller.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Start from root node.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------

    If:
            key > current node

    Then:
            current node can be predecessor

            Move RIGHT

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    If:
            key < current node

    Then:
            current node can be successor

            Move LEFT

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------
    If key is found:

    -------------------------------------------------------------------------------------------------

    For predecessor:
    -------------------------
    Find RIGHTMOST node in LEFT subtree.

    -------------------------------------------------------------------------------------------------

    For successor:
    -------------------------
    Find LEFTMOST node in RIGHT subtree.

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Return predecessor and successor.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    TREE:
    -------------------------

                            5
                          /   \
                         2     10
                        / \    / \
                       1   4  7  12

    key = 10

    =================================================================================================
                                    STEP-BY-STEP EXECUTION
    =================================================================================================

    Current Node:
    -------------------------

            5

    -------------------------------------------------------------------------------------------------

    Since:
    -------------------------

            10 > 5

    Therefore:
    -------------------------

            5 can be predecessor

    Move RIGHT.

    =================================================================================================

    Current Node:
    -------------------------

            10

    -------------------------------------------------------------------------------------------------

    Key found.

    =================================================================================================
                                    FIND PREDECESSOR
    =================================================================================================

    LEFT subtree of 10:
    -------------------------

                            7

    Rightmost node:
    -------------------------

            7

    Therefore:
    -------------------------

            predecessor = 7

    =================================================================================================
                                    FIND SUCCESSOR
    =================================================================================================

    RIGHT subtree of 10:
    -------------------------

                            12

    Leftmost node:
    -------------------------

            12

    Therefore:
    -------------------------

            successor = 12

    =================================================================================================
                                            FINAL ANSWER
    =================================================================================================

            [7, 12]

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for Tree Node
    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;

            left = null;
            right = null;
        }
    }

    static class Solution {

        /*
        =============================================================================================
                        FUNCTION TO FIND PREDECESSOR AND SUCCESSOR
        =============================================================================================
        */

        public int[] succPredBST(TreeNode root, int key) {

            /*
            Store predecessor and successor nodes.
            */

            TreeNode predecessor = null;
            TreeNode successor = null;

            /*
            Current node pointer.
            */

            TreeNode curr = root;

            /*
            Traverse BST.
            */

            while (curr != null) {

                /*
                CASE 1:
                Key is greater than current node.

                Current node can become predecessor.
                */

                if (key > curr.data) {

                    predecessor = curr;

                    curr = curr.right;
                }

                /*
                CASE 2:
                Key is smaller than current node.

                Current node can become successor.
                */

                else if (key < curr.data) {

                    successor = curr;

                    curr = curr.left;
                }

                /*
                CASE 3:
                Key found.
                */

                else {

                    /*
                    Find predecessor from LEFT subtree.

                    Predecessor =
                    RIGHTMOST node in LEFT subtree.
                    */

                    if (curr.left != null) {

                        TreeNode temp = curr.left;

                        while (temp.right != null) {

                            temp = temp.right;
                        }

                        predecessor = temp;
                    }

                    /*
                    Find successor from RIGHT subtree.

                    Successor =
                    LEFTMOST node in RIGHT subtree.
                    */

                    if (curr.right != null) {

                        TreeNode temp = curr.right;

                        while (temp.left != null) {

                            temp = temp.left;
                        }

                        successor = temp;
                    }

                    /*
                    Key processed.
                    */

                    break;
                }
            }

            /*
            Convert nodes to integer values.
            */

            int predVal =
                    (predecessor != null)
                            ? predecessor.data
                            : -1;

            int succVal =
                    (successor != null)
                            ? successor.data
                            : -1;

            /*
            Return answer.
            */

            return new int[]{
                    predVal,
                    succVal
            };
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                20
                              /    \
                            10      30
                           /  \
                          5   15
        */

        TreeNode root = new TreeNode(20);

        root.left = new TreeNode(10);
        root.right = new TreeNode(30);

        root.left.left = new TreeNode(5);
        root.left.right = new TreeNode(15);

        Solution sol = new Solution();

        /*
        Find predecessor and successor of 12.
        */

        int[] result =
                sol.succPredBST(root, 12);

        /*
        Print result.
        */

        System.out.println(
                "Predecessor = " + result[0]
        );

        System.out.println(
                "Successor = " + result[1]
        );
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------

            O(H)

    where:
    -------------------------

            H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    1. Traversing BST takes O(H)

    2. Finding predecessor in left subtree
       takes O(H)

    3. Finding successor in right subtree
       takes O(H)

    -------------------------------------------------------------------------------------------------

    Overall:
    -------------------------

            O(H)

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(1)

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Only constant extra space used.

    No recursion.
    No extra data structures.

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Predecessor:
            Largest smaller value.

    2. Successor:
            Smallest larger value.

    3. BST property helps efficient traversal.

    4. RIGHTMOST node in LEFT subtree
       gives predecessor.

    5. LEFTMOST node in RIGHT subtree
       gives successor.

    6. If predecessor/successor absent:
            return -1

    7. Efficient solution using BST traversal.

    =================================================================================================
    */

}