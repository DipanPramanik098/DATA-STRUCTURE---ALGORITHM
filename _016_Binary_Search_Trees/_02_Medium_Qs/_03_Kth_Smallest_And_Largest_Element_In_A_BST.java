package _016_Binary_Search_Trees._02_Medium_Qs;

import java.util.ArrayList;
import java.util.List;

public class _03_Kth_Smallest_And_Largest_Element_In_A_BST {

    /*
    =================================================================================================
                        KTH SMALLEST AND LARGEST ELEMENT IN BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------
    1. Root node of a Binary Search Tree (BST)
    2. An integer k

    Task:
    -------------------------
    Find:

    1. Kth Smallest Element
    2. Kth Largest Element

    in the BST.

    -------------------------------------------------------------------------------------------------

    Return:
    -------------------------
    Array/List containing:

            [kthSmallest, kthLargest]

    -------------------------------------------------------------------------------------------------

    NOTE:
    -------------------------
    K is 1-indexed.

    Example:
    -------------------------
    k = 1

    means:
    -------------------------
    1st smallest or 1st largest.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                        3
                       / \
                      1   4
                       \
                        2

    k = 1

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [1, 4]

    Explanation:
    -------------------------

    Sorted Order:
    -------------------------

            1 2 3 4

    1st Smallest:
    -------------------------
    1

    1st Largest:
    -------------------------
    4

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            5
                           / \
                          3   6
                         /
                        2
                       /
                      1

    k = 3

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [3, 3]

    Explanation:
    -------------------------

    Sorted Order:
    -------------------------

            1 2 3 5 6

    3rd Smallest:
    -------------------------
    3

    3rd Largest:
    -------------------------
    3

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                        3
                       / \
                      1   4
                       \
                        2

    k = 2

    -------------------------------------------------------------------------------------------------

    Sorted Order:
    -------------------------

            1 2 3 4

    2nd Smallest:
    -------------------------
    2

    2nd Largest:
    -------------------------
    3

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            [2, 3]

    =================================================================================================
                                            INTUITION
    =================================================================================================

    BST Property:
    -------------------------

            Left Subtree < Node < Right Subtree

    -------------------------------------------------------------------------------------------------

    Important Observation:
    -------------------------

    INORDER Traversal of BST gives:
    -------------------------

            Sorted Order (Ascending)

    Traversal Order:
    -------------------------

            LEFT -> ROOT -> RIGHT

    Example:
    -------------------------

                        4
                       / \
                      2   6
                     / \
                    1   3

    Inorder Traversal:
    -------------------------

            1 2 3 4 6

    Sorted order achieved automatically.

    =================================================================================================
                                FINDING KTH SMALLEST ELEMENT
    =================================================================================================

    Use:
    -------------------------
    Normal Inorder Traversal

    Order:
    -------------------------

            LEFT -> ROOT -> RIGHT

    -------------------------------------------------------------------------------------------------

    Logic:
    -------------------------

    1. Visit nodes in sorted order.

    2. Maintain counter k.

    3. Decrease k at every visited node.

    4. When k becomes 0:
       Current node is answer.

    =================================================================================================
                                FINDING KTH LARGEST ELEMENT
    =================================================================================================

    Use:
    -------------------------
    Reverse Inorder Traversal

    Order:
    -------------------------

            RIGHT -> ROOT -> LEFT

    -------------------------------------------------------------------------------------------------

    Why?
    -------------------------
    Reverse inorder gives descending order.

    Example:
    -------------------------

            6 4 3 2 1

    -------------------------------------------------------------------------------------------------

    Logic:
    -------------------------

    1. Visit nodes from largest to smallest.

    2. Maintain counter k.

    3. When k becomes 0:
       Current node is kth largest.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Perform inorder traversal.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Maintain counter.

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------
    When counter becomes 0:

            Store kth smallest.

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------
    Perform reverse inorder traversal.

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Again maintain counter.

    -------------------------------------------------------------------------------------------------

    STEP 6:
    -------------------------
    When counter becomes 0:

            Store kth largest.

    -------------------------------------------------------------------------------------------------

    STEP 7:
    -------------------------
    Return both answers.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    BST:
    -------------------------

                        3
                       / \
                      1   4
                       \
                        2

    k = 2

    =================================================================================================
                                    KTH SMALLEST DRY RUN
    =================================================================================================

    Inorder Traversal:
    -------------------------

            1 -> 2 -> 3 -> 4

    -------------------------------------------------------------------------------------------------

    Visit 1:
    -------------------------
    k = 2 -> 1

    -------------------------------------------------------------------------------------------------

    Visit 2:
    -------------------------
    k = 1 -> 0

    Answer:
    -------------------------
    2nd Smallest = 2

    =================================================================================================
                                    KTH LARGEST DRY RUN
    =================================================================================================

    Reverse Inorder:
    -------------------------

            4 -> 3 -> 2 -> 1

    -------------------------------------------------------------------------------------------------

    Visit 4:
    -------------------------
    k = 2 -> 1

    -------------------------------------------------------------------------------------------------

    Visit 3:
    -------------------------
    k = 1 -> 0

    Answer:
    -------------------------
    2nd Largest = 3

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for BST Node
    static class TreeNode {

        int data;
        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;
            left = null;
            right = null;
        }
    }

    static class Solution {

        /*
        Variable used for tracking kth position.
        */

        private int k;

        /*
        Variable used for storing answer.
        */

        private int result;

        /*
        =============================================================================================
                                FIND KTH SMALLEST ELEMENT
        =============================================================================================
        */

        public int kthSmallest(TreeNode root, int k) {

            this.k = k;

            result = -1;

            inorder(root);

            return result;
        }

        /*
        =============================================================================================
                                    INORDER TRAVERSAL
        =============================================================================================

        LEFT -> ROOT -> RIGHT

        Gives ascending sorted order in BST.
        */

        private void inorder(TreeNode node) {

            if (node != null) {

                /*
                Traverse left subtree.
                */

                inorder(node.left);

                /*
                Current node visited.
                */

                if (--k == 0) {

                    result = node.data;

                    return;
                }

                /*
                Traverse right subtree.
                */

                inorder(node.right);
            }
        }

        /*
        =============================================================================================
                                FIND KTH LARGEST ELEMENT
        =============================================================================================
        */

        public int kthLargest(TreeNode root, int k) {

            this.k = k;

            result = -1;

            reverseInorder(root);

            return result;
        }

        /*
        =============================================================================================
                                REVERSE INORDER TRAVERSAL
        =============================================================================================

        RIGHT -> ROOT -> LEFT

        Gives descending sorted order in BST.
        */

        private void reverseInorder(TreeNode node) {

            if (node != null) {

                /*
                Traverse right subtree first.
                */

                reverseInorder(node.right);

                /*
                Current node visited.
                */

                if (--k == 0) {

                    result = node.data;

                    return;
                }

                /*
                Traverse left subtree.
                */

                reverseInorder(node.left);
            }
        }

        /*
        =============================================================================================
                        FUNCTION TO RETURN BOTH ANSWERS
        =============================================================================================
        */

        public List<Integer> kLargestSmall(TreeNode root, int k) {

            List<Integer> resultList = new ArrayList<>();

            /*
            Add kth smallest.
            */

            resultList.add(kthSmallest(root, k));

            /*
            Add kth largest.
            */

            resultList.add(kthLargest(root, k));

            return resultList;
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                            3
                           / \
                          1   4
                           \
                            2
        */

        TreeNode root = new TreeNode(3);

        root.left = new TreeNode(1);

        root.left.right = new TreeNode(2);

        root.right = new TreeNode(4);

        Solution solution = new Solution();

        int k = 2;

        /*
        Find kth smallest and kth largest.
        */

        List<Integer> result =
                solution.kLargestSmall(root, k);

        /*
        Print result.
        */

        System.out.println(result);
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------
    O(N)

    where:
    -------------------------
    N = Number of nodes in BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    In worst case,
    traversal may visit every node.

    Both inorder and reverse inorder together
    still remain O(N).

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------
    O(H)

    where:
    -------------------------
    H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    Recursive stack space used during traversal.

    -------------------------------------------------------------------------------------------------

    BALANCED BST:
    -------------------------
    H = log N

    Therefore:
    -------------------------
    O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------
    Skewed BST

    Example:
    -------------------------

                10
                  \
                   20
                     \
                      30
                        \
                         40

    Height becomes:
    -------------------------
    O(N)

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Inorder traversal of BST gives sorted order.

    2. Reverse inorder gives descending order.

    3. Kth smallest uses:
       LEFT -> ROOT -> RIGHT

    4. Kth largest uses:
       RIGHT -> ROOT -> LEFT

    5. Counter technique avoids storing all elements.

    6. BST property makes this problem efficient.

    =================================================================================================
    */

}