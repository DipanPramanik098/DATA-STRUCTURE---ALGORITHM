package _016_Binary_Search_Trees._02_Medium_Qs;

public class _04_Check_BST {

    /*
    =================================================================================================
                                    CHECK IF A TREE IS BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------
    Root node of a Binary Tree.

    Task:
    -------------------------
    Determine whether the given tree is a valid
    Binary Search Tree (BST) or not.

    Return:
    -------------------------
    true  -> If tree is BST

    false -> If tree is NOT BST

    =================================================================================================
                                        BST CONDITIONS
    =================================================================================================

    A Binary Tree is called a BST if:

    -------------------------------------------------------------------------------------------------

    CONDITION 1:
    -------------------------
    All nodes in LEFT subtree must be:

            STRICTLY SMALLER

    than current node.

    -------------------------------------------------------------------------------------------------

    CONDITION 2:
    -------------------------
    All nodes in RIGHT subtree must be:

            STRICTLY GREATER

    than current node.

    -------------------------------------------------------------------------------------------------

    CONDITION 3:
    -------------------------
    Both left subtree and right subtree
    must also themselves be valid BSTs.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       2   4     7

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            true

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    LEFT subtree values:
    -------------------------
    2, 3, 4

    All are smaller than 5.

    -------------------------------------------------------------------------------------------------

    RIGHT subtree values:
    -------------------------
    6, 7

    All are greater than 5.

    -------------------------------------------------------------------------------------------------

    Every subtree also follows BST property.

    Therefore:
    -------------------------
    Valid BST.

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       4   2     7

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            false

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    Node 4 is present in LEFT subtree of 3
    but:

            4 > 3

    BST property violated.

    -------------------------------------------------------------------------------------------------

    Node 2 is present in RIGHT subtree of 3
    but:

            2 < 3

    BST property violated.

    Therefore:
    -------------------------
    Not a BST.

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                        2
                       / \
                      1   3

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            true

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            true

    =================================================================================================
                                            INTUITION
    =================================================================================================

    Important Observation:
    -------------------------

    Checking only immediate children is NOT enough.

    Example:
    -------------------------

                            10
                           /  \
                          5    15
                              /
                             6

    -------------------------------------------------------------------------------------------------

    Problem:
    -------------------------

    6 < 15   -> seems correct

    BUT:
    -------------------------

    6 is in RIGHT subtree of 10

    Therefore:
    -------------------------

            6 should be > 10

    BST rule violated.

    -------------------------------------------------------------------------------------------------

    So:
    -------------------------
    Every node must satisfy a VALID RANGE.

    =================================================================================================
                                        RANGE CONCEPT
    =================================================================================================

    For every node:
    -------------------------

            min < node.data < max

    -------------------------------------------------------------------------------------------------

    Initially:
    -------------------------

            (-infinity , +infinity)

    -------------------------------------------------------------------------------------------------

    LEFT SUBTREE:
    -------------------------

            min remains same
            max becomes current node value

    -------------------------------------------------------------------------------------------------

    RIGHT SUBTREE:
    -------------------------

            min becomes current node value
            max remains same

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Start from root.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Assign valid range:

            (-infinity , +infinity)

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------
    For each node:

    Check:
    -------------------------

            min < node.data < max

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------

    LEFT subtree:
    -------------------------

            validate(left, min, node.data)

    -------------------------------------------------------------------------------------------------

    RIGHT subtree:
    -------------------------

            validate(right, node.data, max)

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    If any node violates range:

            return false

    -------------------------------------------------------------------------------------------------

    STEP 6:
    -------------------------
    If all nodes satisfy ranges:

            return true

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    TREE:
    -------------------------

                            5
                          /   \
                         3     6
                        / \     \
                       2   4     7

    =================================================================================================
                                    STEP-BY-STEP EXECUTION
    =================================================================================================

    Root Node = 5

    Valid Range:
    -------------------------

            (-infinity , +infinity)

    5 satisfies range.

    -------------------------------------------------------------------------------------------------

    Move LEFT:
    -------------------------

    Node = 3

    Valid Range:
    -------------------------

            (-infinity , 5)

    3 satisfies range.

    -------------------------------------------------------------------------------------------------

    Move LEFT:
    -------------------------

    Node = 2

    Valid Range:
    -------------------------

            (-infinity , 3)

    2 satisfies range.

    -------------------------------------------------------------------------------------------------

    Move RIGHT:
    -------------------------

    Node = 4

    Valid Range:
    -------------------------

            (3 , 5)

    4 satisfies range.

    -------------------------------------------------------------------------------------------------

    Move RIGHT of root:
    -------------------------

    Node = 6

    Valid Range:
    -------------------------

            (5 , +infinity)

    6 satisfies range.

    -------------------------------------------------------------------------------------------------

    Move RIGHT:
    -------------------------

    Node = 7

    Valid Range:
    -------------------------

            (6 , +infinity)

    7 satisfies range.

    -------------------------------------------------------------------------------------------------

    All nodes valid.

    Result:
    -------------------------

            TRUE

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for Binary Tree Node
    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;

            left = null;
            right = null;
        }
    }

    static class Solution {

        /*
        =============================================================================================
                                    MAIN FUNCTION
        =============================================================================================
        */

        public boolean isBST(TreeNode root) {

            /*
            Initially:
            Entire integer range allowed.
            */

            return validate(
                    root,
                    Long.MIN_VALUE,
                    Long.MAX_VALUE
            );
        }

        /*
        =============================================================================================
                                VALIDATE BST FUNCTION
        =============================================================================================

        This function checks whether every node
        lies within valid range.

                min < node.data < max
        */

        private boolean validate(
                TreeNode node,
                long min,
                long max
        ) {

            /*
            Base Case:
            Empty node is valid BST.
            */

            if (node == null) {

                return true;
            }

            /*
            Check current node range.
            */

            if (node.data <= min ||
                    node.data >= max) {

                return false;
            }

            /*
            Validate LEFT subtree.

            Max becomes current node value.
            */

            boolean leftIsValid =
                    validate(
                            node.left,
                            min,
                            node.data
                    );

            /*
            Validate RIGHT subtree.

            Min becomes current node value.
            */

            boolean rightIsValid =
                    validate(
                            node.right,
                            node.data,
                            max
                    );

            /*
            Both subtrees must be valid.
            */

            return leftIsValid && rightIsValid;
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                7
                              /   \
                             5     10
                            / \    / \
                           3   6  4  15

        This is NOT a BST because:

                4 is in right subtree of 7
                but 4 < 7
        */

        TreeNode root = new TreeNode(7);

        root.left = new TreeNode(5);
        root.right = new TreeNode(10);

        root.left.left = new TreeNode(3);
        root.left.right = new TreeNode(6);

        root.right.left = new TreeNode(4);
        root.right.right = new TreeNode(15);

        Solution solution = new Solution();

        boolean result = solution.isBST(root);

        System.out.println(result);
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------

            O(N)

    where:
    -------------------------
    N = Number of nodes

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    Every node is visited exactly once.

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(H)

    where:
    -------------------------
    H = Height of tree

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    Recursive call stack.

    -------------------------------------------------------------------------------------------------

    BALANCED TREE:
    -------------------------

            H = log N

    Therefore:
    -------------------------

            O(log N)

    -------------------------------------------------------------------------------------------------

    WORST CASE:
    -------------------------
    Skewed Tree

    Example:
    -------------------------

                10
                  \
                   20
                     \
                      30
                        \
                         40

    Height becomes:
    -------------------------

            O(N)

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Every BST is a Binary Tree.

    2. Every Binary Tree is NOT a BST.

    3. BST validation requires RANGE checking.

    4. Immediate child comparison is NOT enough.

    5. Left subtree nodes must all be smaller.

    6. Right subtree nodes must all be greater.

    7. Inorder traversal of BST gives sorted order.

    8. Recursive min-max validation is the most
       efficient and standard approach.

    =================================================================================================
    */

}