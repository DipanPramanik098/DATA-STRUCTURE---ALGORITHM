package _016_Binary_Search_Trees._03_FAQs;

import java.util.Stack;

public class _01_BST_Iterator {

    /*
    =================================================================================================
                                            BST ITERATOR
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Implement a BSTIterator class that performs
    INORDER TRAVERSAL of a Binary Search Tree (BST).

    -------------------------------------------------------------------------------------------------

    The iterator should support:

    1. BSTIterator(TreeNode root)
            -> Constructor

    2. boolean hasNext()
            -> Returns true if next element exists

    3. int next()
            -> Returns next smallest element

    =================================================================================================
                                    IMPORTANT OBSERVATION
    =================================================================================================

    Inorder Traversal of BST gives:

            SORTED ORDER

    -------------------------------------------------------------------------------------------------

    Inorder Traversal:
    -------------------------

            LEFT -> ROOT -> RIGHT

    -------------------------------------------------------------------------------------------------

    Therefore:

            BST Iterator returns elements
            in increasing sorted order.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input BST:

                                7
                              /   \
                             3     15
                                  /  \
                                 9   20

    -------------------------------------------------------------------------------------------------

    Operations:
    -------------------------

    BSTIterator iterator = new BSTIterator(root);

    iterator.next();      -> 3
    iterator.next();      -> 7
    iterator.hasNext();   -> true
    iterator.next();      -> 9
    iterator.hasNext();   -> true
    iterator.next();      -> 15
    iterator.hasNext();   -> true
    iterator.next();      -> 20
    iterator.hasNext();   -> false

    =================================================================================================
                                            OUTPUT
    =================================================================================================

            3 7 9 15 20

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

    [ "BSTIterator" ,
      "next" ,
      "next" ,
      "next",
      "next" ,
      "next" ,
      "hasNext" ]

    BST:
    -------------------------

                                7
                              /   \
                             3     15
                                  /  \
                                 9   20

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            [3, 7, 9, 15, 20, false]

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            [3, 7, 9, 15, 20, false]

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    All elements are traversed.

    Therefore:
            No next element exists.

    =================================================================================================
                                            INTUITION
    =================================================================================================

    Normal inorder traversal requires:

            O(N) space

    because:
            We store all nodes.

    -------------------------------------------------------------------------------------------------

    But we can optimize it.

    =================================================================================================
                                    OPTIMIZED IDEA
    =================================================================================================

    Instead of storing entire inorder traversal:

            Store ONLY required nodes.

    -------------------------------------------------------------------------------------------------

    Use STACK.

    -------------------------------------------------------------------------------------------------

    Why stack?

    Because inorder traversal naturally follows:

            LEFT -> ROOT -> RIGHT

    and stack helps simulate recursion.

    =================================================================================================
                                        CORE IDEA
    =================================================================================================

    Initially:
    -------------------------

    Push all LEFT nodes into stack.

    -------------------------------------------------------------------------------------------------

    Smallest element becomes TOP of stack.

    -------------------------------------------------------------------------------------------------

    Whenever next() is called:

    1. Pop top node
    2. Return its value
    3. Move to its RIGHT subtree
    4. Again push all LEFT nodes

    -------------------------------------------------------------------------------------------------

    This ensures:

            Next smallest element is always on top.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------

    Constructor:
    -------------------------

    Push all left nodes from root into stack.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------

    hasNext():
    -------------------------

    If stack is not empty:
            return true

    else:
            return false

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    next():
    -------------------------

    1. Pop top node
    2. Store its value
    3. Push all left nodes of right subtree
    4. Return stored value

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    BST:

                                7
                              /   \
                             3     15
                                  /  \
                                 9   20

    =================================================================================================
                                    CONSTRUCTOR EXECUTION
    =================================================================================================

    pushAll(7)

    Stack Operations:
    -------------------------

    push(7)
    move left

    push(3)
    move left

    NULL reached.

    -------------------------------------------------------------------------------------------------

    Stack:

            TOP -> 3
                    7

    =================================================================================================
                                        next()
    =================================================================================================

    Pop:
            3

    Return:
            3

    -------------------------------------------------------------------------------------------------

    3 has no right subtree.

    -------------------------------------------------------------------------------------------------

    Stack:

            TOP -> 7

    =================================================================================================
                                        next()
    =================================================================================================

    Pop:
            7

    Return:
            7

    -------------------------------------------------------------------------------------------------

    Move to right subtree:
            15

    Push all left nodes.

    -------------------------------------------------------------------------------------------------

    push(15)
    move left

    push(9)

    -------------------------------------------------------------------------------------------------

    Stack:

            TOP -> 9
                    15

    =================================================================================================
                                        next()
    =================================================================================================

    Pop:
            9

    Return:
            9

    -------------------------------------------------------------------------------------------------

    Stack:

            TOP -> 15

    =================================================================================================
                                        next()
    =================================================================================================

    Pop:
            15

    Return:
            15

    -------------------------------------------------------------------------------------------------

    Move to right subtree:
            20

    push(20)

    -------------------------------------------------------------------------------------------------

    Stack:

            TOP -> 20

    =================================================================================================
                                        next()
    =================================================================================================

    Pop:
            20

    Return:
            20

    -------------------------------------------------------------------------------------------------

    Stack becomes empty.

    =================================================================================================
                                        hasNext()
    =================================================================================================

    Stack empty:
            false

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    /*
    =============================================================================================
                                        TREE NODE
    =============================================================================================
    */

    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;

            left = null;
            right = null;
        }
    }

    /*
    =============================================================================================
                                        BST ITERATOR
    =============================================================================================
    */

    static class BSTIterator {

        /*
        Stack stores nodes needed for traversal.
        */

        private Stack<TreeNode> stack = new Stack<>();

        /*
        =========================================================================================
                                        CONSTRUCTOR
        =========================================================================================

        Push all left nodes into stack.

        Smallest element reaches top.
        */

        public BSTIterator(TreeNode root) {

            pushAll(root);
        }

        /*
        =========================================================================================
                                        hasNext()
        =========================================================================================

        Returns true if stack contains nodes.
        */

        public boolean hasNext() {

            return !stack.isEmpty();
        }

        /*
        =========================================================================================
                                            next()
        =========================================================================================

        1. Pop smallest node
        2. Process its right subtree
        3. Return node value
        */

        public int next() {

            /*
            Get top node.
            */

            TreeNode temp = stack.pop();

            /*
            Push all left nodes
            of right subtree.
            */

            pushAll(temp.right);

            /*
            Return current value.
            */

            return temp.data;
        }

        /*
        =========================================================================================
                                    HELPER FUNCTION
        =========================================================================================

        Push all left nodes into stack.
        */

        private void pushAll(TreeNode node) {

            while (node != null) {

                stack.push(node);

                node = node.left;
            }
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                7
                              /   \
                             3     15
                                  /  \
                                 9   20
        */

        TreeNode root = new TreeNode(7);

        root.left = new TreeNode(3);

        root.right = new TreeNode(15);

        root.right.left = new TreeNode(9);

        root.right.right = new TreeNode(20);

        /*
        Create iterator.
        */

        BSTIterator iterator = new BSTIterator(root);

        /*
        Traverse BST in sorted order.
        */

        System.out.println("BST Elements in Sorted Order:");

        while (iterator.hasNext()) {

            System.out.print(iterator.next() + " ");
        }
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    next():
    -------------------------

            O(1) Amortized

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    Every node is pushed and popped exactly once.

    Therefore:
            Total operations = O(N)

    Hence:
            Amortized O(1)

    -------------------------------------------------------------------------------------------------

    hasNext():
    -------------------------

            O(1)

    because:
            Only checking stack empty or not.

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(H)

    where:
    -------------------------

            H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Stack stores path from root
    to leftmost node.

    -------------------------------------------------------------------------------------------------

    Best Case:
    -------------------------

            O(log N)

    for balanced BST.

    -------------------------------------------------------------------------------------------------

    Worst Case:
    -------------------------

            O(N)

    for skewed BST.

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. BST inorder traversal gives sorted order.

    2. Stack simulates recursion.

    3. We do NOT store full traversal.

    4. Only required nodes are stored.

    5. Space optimized solution.

    6. next() returns next smallest element.

    7. hasNext() checks remaining nodes.

    8. Excellent interview problem on BST + Stack.

    =================================================================================================
    */

}