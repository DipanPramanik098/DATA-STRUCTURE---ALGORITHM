package _016_Binary_Search_Trees._03_FAQs;

import java.util.Stack;

public class _02_Two_Sum_In_BST {

    /*
    =================================================================================================
                                            TWO SUM IN BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given:
    -------------------------

            1. Root of a Binary Search Tree (BST)
            2. An integer k

    -------------------------------------------------------------------------------------------------

    Task:
    -------------------------

    Return:
            true

    if there exist TWO nodes in BST such that:

            node1 + node2 = k

    -------------------------------------------------------------------------------------------------

    Otherwise return:

            false

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input BST:
    -------------------------

                                5
                              /   \
                             3     6
                            / \     \
                           2   4     7

    k = 9

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            true

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    Multiple valid pairs exist:

            3 + 6 = 9
            5 + 4 = 9
            2 + 7 = 9

    Therefore:
            return true

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input BST:
    -------------------------

                                5
                              /   \
                             3     6
                            / \     \
                           2   4     7

    k = 14

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            false

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

    No pair exists whose sum = 14

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                                5
                              /   \
                             3     6
                            / \     \
                           2   4     7

    k = 12

    -------------------------------------------------------------------------------------------------

    Output:
    -------------------------

            true

    -------------------------------------------------------------------------------------------------

    Correct Answer:
    -------------------------

            true

    -------------------------------------------------------------------------------------------------

    Explanation:
    -------------------------

            5 + 7 = 12

    =================================================================================================
                                            INTUITION
    =================================================================================================

    We know:

            Inorder traversal of BST gives sorted order.

    -------------------------------------------------------------------------------------------------

    Sorted order for given BST:

            2 3 4 5 6 7

    -------------------------------------------------------------------------------------------------

    This problem becomes similar to:

            TWO SUM in SORTED ARRAY

    =================================================================================================
                                    NORMAL APPROACH
    =================================================================================================

    1. Store inorder traversal in array.
    2. Apply two pointer approach.

    -------------------------------------------------------------------------------------------------

    But:
            This uses O(N) extra space.

    =================================================================================================
                                    OPTIMIZED APPROACH
    =================================================================================================

    Instead of storing entire inorder traversal:

            Use TWO BST Iterators

    -------------------------------------------------------------------------------------------------

    Iterator 1:
    -------------------------

            Gives next smallest element
            (Normal inorder traversal)

    -------------------------------------------------------------------------------------------------

    Iterator 2:
    -------------------------

            Gives next largest element
            (Reverse inorder traversal)

    -------------------------------------------------------------------------------------------------

    This simulates:

            Two Pointer Technique

    directly on BST.

    =================================================================================================
                                        KEY IDEA
    =================================================================================================

    We maintain:

            i -> smallest value
            j -> largest value

    -------------------------------------------------------------------------------------------------

    If:
    -------------------------

            i + j == k

    return true

    -------------------------------------------------------------------------------------------------

    If:
    -------------------------

            i + j < k

    move left iterator forward
    to get larger value.

    -------------------------------------------------------------------------------------------------

    If:
    -------------------------

            i + j > k

    move right iterator backward
    to get smaller value.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------

    Create two BST Iterators.

    -------------------------------------------------------------------------------------------------

    Iterator l:
    -------------------------

            Normal inorder traversal

            Gives increasing values.

    -------------------------------------------------------------------------------------------------

    Iterator r:
    -------------------------

            Reverse inorder traversal

            Gives decreasing values.

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------

    Initialize:

            i = smallest element
            j = largest element

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    While:
            i < j

    -------------------------------------------------------------------------------------------------

    CASE 1:
    -------------------------

    If:
            i + j == k

    return true

    -------------------------------------------------------------------------------------------------

    CASE 2:
    -------------------------

    If:
            i + j < k

    move left iterator ahead.

    -------------------------------------------------------------------------------------------------

    CASE 3:
    -------------------------

    If:
            i + j > k

    move right iterator backward.

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------

    If loop ends:
            return false

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    BST:
    -------------------------

                                5
                              /   \
                             3     6
                            / \     \
                           2   4     7

    k = 9

    =================================================================================================
                                    INITIALIZATION
    =================================================================================================

    Left Iterator:
    -------------------------

            gives smallest value = 2

    Right Iterator:
    -------------------------

            gives largest value = 7

    -------------------------------------------------------------------------------------------------

    i = 2
    j = 7

    -------------------------------------------------------------------------------------------------

    i + j = 9

    Target:
            9

    -------------------------------------------------------------------------------------------------

    Condition satisfied.

    Return:
            true

    =================================================================================================
                                            BST ITERATOR
    =================================================================================================

    We create a custom iterator.

    -------------------------------------------------------------------------------------------------

    For Normal Inorder:
    -------------------------

            LEFT -> ROOT -> RIGHT

    Gives increasing order.

    -------------------------------------------------------------------------------------------------

    For Reverse Inorder:
    -------------------------

            RIGHT -> ROOT -> LEFT

    Gives decreasing order.

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    /*
    =============================================================================================
                                            TREE NODE
    =============================================================================================
    */

    static class TreeNode {

        int data;

        TreeNode left;
        TreeNode right;

        TreeNode(int val) {

            data = val;

            left = null;
            right = null;
        }
    }

    /*
    =============================================================================================
                                        BST ITERATOR
    =============================================================================================

    This iterator can work in:

            1. Normal inorder
            2. Reverse inorder

    depending on reverse flag.
    */

    static class BSTIterator {

        /*
        Stack used for traversal.
        */

        private Stack<TreeNode> stack;

        /*
        reverse = false
                -> Normal inorder

        reverse = true
                -> Reverse inorder
        */

        private boolean reverse;

        /*
        =========================================================================================
                                            CONSTRUCTOR
        =========================================================================================
        */

        public BSTIterator(TreeNode root, boolean isReverse) {

            stack = new Stack<>();

            reverse = isReverse;

            pushAll(root);
        }

        /*
        =========================================================================================
                                        PUSH ALL FUNCTION
        =========================================================================================

        If reverse == false:
                push all LEFT nodes

        If reverse == true:
                push all RIGHT nodes
        */

        private void pushAll(TreeNode node) {

            while (node != null) {

                stack.push(node);

                /*
                Normal inorder:
                        move LEFT

                Reverse inorder:
                        move RIGHT
                */

                node = (reverse)
                        ? node.right
                        : node.left;
            }
        }

        /*
        =========================================================================================
                                            hasNext()
        =========================================================================================
        */

        public boolean hasNext() {

            return !stack.isEmpty();
        }

        /*
        =========================================================================================
                                                next()
        =========================================================================================

        Returns:
                next smallest OR next largest
        */

        public int next() {

            /*
            Get top node.
            */

            TreeNode node = stack.pop();

            /*
            Normal inorder:
                    process RIGHT subtree

            Reverse inorder:
                    process LEFT subtree
            */

            if (!reverse) {

                pushAll(node.right);
            }

            else {

                pushAll(node.left);
            }

            /*
            Return current node value.
            */

            return node.data;
        }
    }

    /*
    =============================================================================================
                                            SOLUTION
    =============================================================================================
    */

    static class Solution {

        /*
        =========================================================================================
                                    TWO SUM IN BST
        =========================================================================================
        */

        public boolean twoSumBST(TreeNode root, int k) {

            /*
            Edge case:
            Empty tree.
            */

            if (root == null) {

                return false;
            }

            /*
            Left iterator:
                    smallest -> largest
            */

            BSTIterator leftIterator =
                    new BSTIterator(root, false);

            /*
            Right iterator:
                    largest -> smallest
            */

            BSTIterator rightIterator =
                    new BSTIterator(root, true);

            /*
            Initialize pointers.
            */

            int i = leftIterator.next();

            int j = rightIterator.next();

            /*
            Continue until pointers cross.
            */

            while (i < j) {

                int sum = i + j;

                /*
                Pair found.
                */

                if (sum == k) {

                    return true;
                }

                /*
                Need larger sum.
                */

                else if (sum < k) {

                    i = leftIterator.next();
                }

                /*
                Need smaller sum.
                */

                else {

                    j = rightIterator.next();
                }
            }

            /*
            No valid pair found.
            */

            return false;
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                5
                              /   \
                             3     6
                            / \     \
                           2   4     7
        */

        TreeNode root = new TreeNode(5);

        root.left = new TreeNode(3);
        root.right = new TreeNode(6);

        root.left.left = new TreeNode(2);
        root.left.right = new TreeNode(4);

        root.right.right = new TreeNode(7);

        /*
        Create solution object.
        */

        Solution solution = new Solution();

        /*
        Target sum.
        */

        int k = 9;

        /*
        Check result.
        */

        boolean result =
                solution.twoSumBST(root, k);

        /*
        Print answer.
        */

        System.out.println(result
                ? "True"
                : "False");
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------

            O(N)

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Every node is processed at most once
    by iterators.

    Therefore:
            Total operations = O(N)

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------

            O(H)

    where:
    -------------------------

            H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------

    Stack stores nodes along tree height.

    -------------------------------------------------------------------------------------------------

    Balanced BST:
    -------------------------

            O(log N)

    -------------------------------------------------------------------------------------------------

    Skewed BST:
    -------------------------

            O(N)

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Inorder traversal gives sorted order.

    2. Reverse inorder gives reverse sorted order.

    3. Two iterators simulate two pointers.

    4. No extra array required.

    5. Space optimized solution.

    6. Excellent combination of:
            BST + Stack + Two Pointers

    7. Very important interview problem.

    =================================================================================================
    */

}