package _016_Binary_Search_Trees._01_Theory_And_Basics;

import java.util.*;

public class _03_Floor_And_Ceil_In_A_BST {

    /*
    =================================================================================================
                                FLOOR AND CEIL IN A BST
    =================================================================================================

    PROBLEM STATEMENT
    -------------------------------------------------------------------------------------------------

    Given the root of a Binary Search Tree (BST) and a key value,
    find:

    1. Floor Value
    2. Ceil Value

    -------------------------------------------------------------------------------------------------

    FLOOR:
    -------------------------
    Greatest value smaller than or equal to the key.

    -------------------------------------------------------------------------------------------------

    CEIL:
    -------------------------
    Smallest value greater than or equal to the key.

    -------------------------------------------------------------------------------------------------

    If floor or ceil does not exist,
    return -1.

    =================================================================================================
                                            EXAMPLE 1
    =================================================================================================

    Input:
    -------------------------

                            8
                          /   \
                         4     12
                        / \    / \
                       2   6  10 14

    key = 11

    Output:
    -------------------------

            [10, 12]

    Explanation:
    -------------------------

    Floor of 11:
    -------------------------
    Largest value <= 11 = 10

    Ceil of 11:
    -------------------------
    Smallest value >= 11 = 12

    =================================================================================================
                                            EXAMPLE 2
    =================================================================================================

    Input:
    -------------------------

                            8
                          /   \
                         4     12
                        / \    / \
                       2   6  10 14

    key = 15

    Output:
    -------------------------

            [14, -1]

    Explanation:
    -------------------------

    Floor of 15:
    -------------------------
    Largest value <= 15 = 14

    Ceil of 15:
    -------------------------
    No value >= 15 exists.

    Therefore:
    -------------------------
    Ceil = -1

    =================================================================================================
                                        PRACTICE QUESTION
    =================================================================================================

    Input:
    -------------------------

                            8
                          /   \
                         4     12
                        / \    / \
                       2   6  10 14

    key = 1

    Output:
    -------------------------

            [-1, 2]

    Explanation:
    -------------------------

    Floor:
    -------------------------
    No value smaller than or equal to 1 exists.

    Therefore:
    -------------------------
    Floor = -1

    Ceil:
    -------------------------
    Smallest value greater than or equal to 1 = 2

    Correct Answer:
    -------------------------

            [-1, 2]

    =================================================================================================
                                            INTUITION
    =================================================================================================

    BST Property:
    -------------------------

            Left Subtree < Node < Right Subtree

    This property helps us efficiently search for floor and ceil.

    -------------------------------------------------------------------------------------------------

    FLOOR LOGIC
    -------------------------------------------------------------------------------------------------

    Goal:
    -------------------------
    Find largest value <= key.

    -------------------------------------------------------------------------------------------------

    CASE 1:
    -------------------------
    current.data == key

    Then:
    -------------------------
    Exact match found.

    Floor = current.data

    -------------------------------------------------------------------------------------------------

    CASE 2:
    -------------------------
    current.data < key

    Then:
    -------------------------
    Current node can be a possible floor.

    Store current value.

    Move RIGHT to search for larger possible floor.

    -------------------------------------------------------------------------------------------------

    CASE 3:
    -------------------------
    current.data > key

    Then:
    -------------------------
    Current node cannot be floor.

    Move LEFT.

    =================================================================================================
                                            CEIL LOGIC
    =================================================================================================

    Goal:
    -------------------------
    Find smallest value >= key.

    -------------------------------------------------------------------------------------------------

    CASE 1:
    -------------------------
    current.data == key

    Then:
    -------------------------
    Exact match found.

    Ceil = current.data

    -------------------------------------------------------------------------------------------------

    CASE 2:
    -------------------------
    current.data > key

    Then:
    -------------------------
    Current node can be possible ceil.

    Store current value.

    Move LEFT to search for smaller possible ceil.

    -------------------------------------------------------------------------------------------------

    CASE 3:
    -------------------------
    current.data < key

    Then:
    -------------------------
    Current node cannot be ceil.

    Move RIGHT.

    =================================================================================================
                                            APPROACH
    =================================================================================================

    STEP 1:
    -------------------------
    Initialize:

            floor = -1
            ceil = -1

    -------------------------------------------------------------------------------------------------

    STEP 2:
    -------------------------
    Find FLOOR.

    Traverse BST from root.

    -------------------------------------------------------------------------------------------------

    STEP 3:
    -------------------------

    If:
    -------------------------
    current.data == key

    Then:
    -------------------------
    floor = current.data

    -------------------------------------------------------------------------------------------------

    If:
    -------------------------
    current.data < key

    Then:
    -------------------------
    Store current node as floor.

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    Else:
    -------------------------
    Move LEFT.

    -------------------------------------------------------------------------------------------------

    STEP 4:
    -------------------------
    Find CEIL using similar logic.

    -------------------------------------------------------------------------------------------------

    If:
    -------------------------
    current.data > key

    Then:
    -------------------------
    Store current node as ceil.

    Move LEFT.

    -------------------------------------------------------------------------------------------------

    Else:
    -------------------------
    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    STEP 5:
    -------------------------
    Return floor and ceil.

    =================================================================================================
                                            DRY RUN
    =================================================================================================

    BST:
    -------------------------

                            8
                          /   \
                         4     12
                        / \    / \
                       2   6  10 14

    key = 11

    =================================================================================================
                                        FLOOR DRY RUN
    =================================================================================================

    Current = 8

    Compare:
    -------------------------
    8 < 11

    Possible floor = 8

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    Current = 12

    Compare:
    -------------------------
    12 > 11

    Move LEFT.

    -------------------------------------------------------------------------------------------------

    Current = 10

    Compare:
    -------------------------
    10 < 11

    Possible floor = 10

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    Current = NULL

    Final Floor:
    -------------------------
    10

    =================================================================================================
                                        CEIL DRY RUN
    =================================================================================================

    Current = 8

    Compare:
    -------------------------
    8 < 11

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    Current = 12

    Compare:
    -------------------------
    12 > 11

    Possible ceil = 12

    Move LEFT.

    -------------------------------------------------------------------------------------------------

    Current = 10

    Compare:
    -------------------------
    10 < 11

    Move RIGHT.

    -------------------------------------------------------------------------------------------------

    Current = NULL

    Final Ceil:
    -------------------------
    12

    =================================================================================================
                                            JAVA CODE
    =================================================================================================
    */

    // Definition for BST Node
    static class TreeNode {

        int data;
        TreeNode left;
        TreeNode right;

        TreeNode(int val) {
            data = val;
            left = null;
            right = null;
        }
    }

    static class Solution {

        public List<Integer> floorCeilOfBST(TreeNode root, int key) {

            /*
            Initialize floor and ceil.

            -1 means:
            value not found.
            */

            int floor = -1;
            int ceil = -1;

            /*
            =========================================================================================
                                            FIND FLOOR
            =========================================================================================
            */

            TreeNode current = root;

            while (current != null) {

                /*
                Exact match found.
                */

                if (current.data == key) {
                    floor = current.data;
                    break;
                }

                /*
                Current node can be possible floor.
                Move RIGHT for larger floor.
                */

                else if (current.data < key) {

                    floor = current.data;

                    current = current.right;
                }

                /*
                Move LEFT for smaller values.
                */

                else {
                    current = current.left;
                }
            }

            /*
            =========================================================================================
                                            FIND CEIL
            =========================================================================================
            */

            current = root;

            while (current != null) {

                /*
                Exact match found.
                */

                if (current.data == key) {

                    ceil = current.data;

                    break;
                }

                /*
                Current node can be possible ceil.
                Move LEFT for smaller ceil.
                */

                else if (current.data > key) {

                    ceil = current.data;

                    current = current.left;
                }

                /*
                Move RIGHT for larger values.
                */

                else {
                    current = current.right;
                }
            }

            /*
            Return floor and ceil values.
            */

            return Arrays.asList(floor, ceil);
        }
    }

    /*
    =================================================================================================
                                            MAIN METHOD
    =================================================================================================
    */

    public static void main(String[] args) {

        /*
                                8
                              /   \
                             4     12
                            / \    / \
                           2   6  10 14
        */

        TreeNode root = new TreeNode(8);

        root.left = new TreeNode(4);
        root.right = new TreeNode(12);

        root.left.left = new TreeNode(2);
        root.left.right = new TreeNode(6);

        root.right.left = new TreeNode(10);
        root.right.right = new TreeNode(14);

        Solution sol = new Solution();

        int key = 11;

        List<Integer> result = sol.floorCeilOfBST(root, key);

        System.out.println("Floor = " + result.get(0));
        System.out.println("Ceil = " + result.get(1));
    }

    /*
    =================================================================================================
                                        COMPLEXITY ANALYSIS
    =================================================================================================

    TIME COMPLEXITY
    -------------------------------------------------------------------------------------------------

    Time Complexity:
    -------------------------
    O(H)

    where:
    -------------------------
    H = Height of BST

    -------------------------------------------------------------------------------------------------

    Reason:
    -------------------------
    We traverse downward only once
    for floor search and once for ceil search.

    In Balanced BST:
    -------------------------
    H = log N

    Therefore:
    -------------------------
    O(log N)

    -------------------------------------------------------------------------------------------------

    In Worst Case:
    -------------------------
    BST becomes skewed.

    Example:
    -------------------------

                10
                  \
                   20
                     \
                      30
                        \
                         40

    Height becomes:
    -------------------------
    O(N)

    =================================================================================================
                                        SPACE COMPLEXITY
    =================================================================================================

    Space Complexity:
    -------------------------
    O(1)

    Reason:
    -------------------------
    No recursion stack used.

    Only few variables are maintained.

    =================================================================================================
                                            KEY POINTS
    =================================================================================================

    1. Floor = Largest smaller or equal value.

    2. Ceil = Smallest greater or equal value.

    3. BST property helps avoid unnecessary traversal.

    4. Balanced BST gives O(log N) performance.

    5. Iterative solution is memory efficient.

    =================================================================================================
    */

}